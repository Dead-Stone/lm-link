/**
 * Bundles Simple Icons + custom SVG paths into lib/brand-logo-paths.ts (offline, no CDN).
 * Run: npm run sync-brand-logos
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { createRequire } from "module";

const require = createRequire(import.meta.url);
const icons = require("simple-icons");

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.join(__dirname, "../lib/brand-logo-paths.ts");
const CUSTOM_SRC = path.join(__dirname, "../lib/custom-brand-logos.ts");

/** App brand key -> simple-icons export name */
const SIMPLE_ICON_MAP = {
  meta: "siMeta",
  google: "siGoogle",
  mistral: "siMistralai",
  qwen: "siQwen",
  deepseek: "siDeepseek",
  nvidia: "siNvidia",
  apple: "siApple",
  anthropic: "siAnthropic",
  lmstudio: "siLmstudio",
  alibaba: "siAlibabacloud",
  bytedance: "siBytedance",
  perplexity: "siPerplexity",
};

/** Parse CUSTOM_BRAND_LOGOS from lib/custom-brand-logos.ts without a TS runner. */
function loadCustomLogos() {
  const src = fs.readFileSync(CUSTOM_SRC, "utf8");
  const block = src.match(
    /export const CUSTOM_BRAND_LOGOS[^=]*=\s*\{([\s\S]*?)\n\};/
  );
  if (!block) return {};

  const out = {};
  const entryRe = /(\w+):\s*\{\s*hex:\s*"([^"]+)",\s*path:\s*"((?:\\.|[^"\\])*)"/g;
  let m;
  while ((m = entryRe.exec(block[1])) !== null) {
    out[m[1]] = { hex: m[2], path: m[3].replace(/\\"/g, '"') };
  }
  return out;
}

const merged = new Map();

for (const [key, exportName] of Object.entries(SIMPLE_ICON_MAP)) {
  const icon = icons[exportName];
  if (!icon?.path) {
    console.warn(`Missing simple-icons export: ${exportName} (${key})`);
    continue;
  }
  merged.set(key, { hex: icon.hex, path: icon.path });
}

for (const [key, logo] of Object.entries(loadCustomLogos())) {
  merged.set(key, logo);
}

const keys = [...merged.keys()].sort();
const body = `/** Generated by scripts/sync-brand-logos.mjs — do not edit by hand. */
import { ModelBrandKey } from "./model-provider-logos";

export type BundledBrandLogo = {
  path: string;
  hex: string;
};

export const BUNDLED_BRAND_LOGOS: Partial<Record<ModelBrandKey, BundledBrandLogo>> = {
${keys
  .map((key) => {
    const { hex, path: d } = merged.get(key);
    return `  ${key}: { hex: "${hex}", path: ${JSON.stringify(d)} },`;
  })
  .join("\n")}
};
`;

fs.writeFileSync(OUT, body);
console.log(`Wrote ${keys.length} brand logos to ${path.relative(process.cwd(), OUT)}`);
